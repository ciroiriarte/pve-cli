// Command coverage writes docs/coverage.md: for the embedded PVE and PDM
// schemas, which endpoints are wrapped by a curated pc command vs reachable
// only via `pc raw` / `pc api`. Run via `make coverage`.
package main

import (
	"fmt"
	"os"
	"strings"

	"github.com/ciroiriarte/pve-cli/internal/coverage"
	pdm "github.com/ciroiriarte/pve-cli/internal/generated/pdm"
	pve "github.com/ciroiriarte/pve-cli/internal/generated/pve"
	"github.com/ciroiriarte/pve-cli/internal/schema"
)

func main() {
	out := "docs/coverage.md"
	if len(os.Args) > 1 {
		out = os.Args[1]
	}
	pveAPI, err := pve.Schema()
	if err != nil {
		fail(err)
	}
	pdmAPI, err := pdm.Schema()
	if err != nil {
		fail(err)
	}

	var b strings.Builder
	b.WriteString("# API coverage\n\n")
	b.WriteString("Generated by `make coverage` from the embedded schemas. Each endpoint is\n")
	b.WriteString("either wrapped by a **curated** `pc` command or reachable only via the\n")
	b.WriteString("`pc raw` / `pc api` escape hatch. The source of truth for \"curated\" is\n")
	b.WriteString("`internal/coverage/registry.go`.\n\n")

	section(&b, "Proxmox VE", pveAPI)
	section(&b, "Proxmox Datacenter Manager (PDM)", pdmAPI)

	if err := os.WriteFile(out, []byte(b.String()), 0o644); err != nil {
		fail(err)
	}
	fmt.Printf("wrote %s\n", out)
}

func section(b *strings.Builder, title string, api *schema.API) {
	var curated, raw []string
	for _, e := range api.Endpoints {
		if cmd, ok := coverage.Classify(e.Method, e.Path); ok {
			curated = append(curated, fmt.Sprintf("| `%s %s` | %s |", e.Method, e.Path, cmd))
		} else {
			raw = append(raw, fmt.Sprintf("`%s %s`", e.Method, e.Path))
		}
	}
	total := len(api.Endpoints)
	pct := 0.0
	if total > 0 {
		pct = 100 * float64(len(curated)) / float64(total)
	}
	fmt.Fprintf(b, "## %s\n\n", title)
	fmt.Fprintf(b, "- Total endpoints: **%d**\n- Curated: **%d** (%.0f%%)\n- Raw-only: **%d**\n\n",
		total, len(curated), pct, len(raw))

	b.WriteString("### Curated\n\n| Endpoint | Command |\n|---|---|\n")
	b.WriteString(strings.Join(curated, "\n"))
	b.WriteString("\n\n<details><summary>Raw-only endpoints (")
	fmt.Fprintf(b, "%d) — reachable via `pc raw` / `pc api`</summary>\n\n", len(raw))
	b.WriteString(strings.Join(raw, " · "))
	b.WriteString("\n\n</details>\n\n")
}

func fail(err error) {
	fmt.Fprintln(os.Stderr, "coverage:", err)
	os.Exit(1)
}
